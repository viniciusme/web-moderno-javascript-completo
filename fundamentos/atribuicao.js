console.log(b);

b -= 4; // Aqui estamos subtraindo 4 do valor de b => b = b - 4
console.log(b);

b *= 2;
console.log(b); // b = b * 2

b /= 2;
console.log(b); // b = b / 2

b %= 2; // b = b % 2
